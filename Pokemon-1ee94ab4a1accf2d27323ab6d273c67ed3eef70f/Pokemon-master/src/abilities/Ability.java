package abilities;

import pokemon.Type;

public class Ability {
    public int damage;
    public String abilityName;

    public Ability() {
        this.damage = damage;
        this.abilityName = abilityName;
    }

    public int getDamage() {
        return damage;
    }

    public String getAbilityName() {
        return abilityName;
    }
}